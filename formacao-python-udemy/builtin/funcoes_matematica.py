import math

num1 = -10
num2 = 10
print(abs(num1))
print(abs(num2))
print(abs(num1) + abs(num2)) # Absoluto que desconsidera o sinal

maior_valor = max(10,20,30) # Traz o maior valor
print(maior_valor)

lista = [0,2,3,4,5,6]
print(max(lista))

print(min(lista)) # Traz o menor valor

x = 2
y = 3
print(pow(x,y)) # Potência de um número

print(math.sqrt(25)) # Raiz quadrada

print(round(2.455,2)) # Arredondamento

print(math.floor(2.4566)) # Arredondamento para baixo
print(math.ceil(2.4566)) # Arredondamento para cima