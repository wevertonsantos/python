# Operadores aritméticos

print(10 + 20)
soma = 10 + 10.5
print(soma)

numero1 = 10
numero2 = 20
soma = numero1 + numero2
print(soma)

subtracao = 20 - 30
print(subtracao)

multiplicacao = 20 * 2
print(multiplicacao)

divisao = 10 / 3
print(divisao)

# Parte inteira da divisão
resultado = 10 // 3
print(resultado)

# Exponenciação
resultado = 2 ** 4

# Resto
resultado = 4 % 2
print(resultado)

'''
Ordem de prioridade
1 - **
2 - *
3 - /
4 - %
5 - //

Quando tem parênteses, o python resolve primeiro do parênteses
'''

resultado = 10 * 2 + 1
print(resultado)

resultado = 10 * (2 + 1)
print(resultado)

i = 1
i = i + 1
print(i)

x = 1
x += 1
print(x)