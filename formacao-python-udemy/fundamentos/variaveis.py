# Declarando variável como numero
numero = 1
numero123 = 2
print(numero,numero123)

# Variável como string
texto = "Olá mundo"
print(texto)

# Tipos primitivos

variavel = None # variável sem nada
print(variavel)

inteiro = 10 # tipo inteiro
print(inteiro)

decimal = 1.53 # tipo float
print(decimal)

texto = "texto" # tipo string
print(texto)

# Variável booleana
var = True # valor verdadeiro
print(var)
var = False # valor falso
print(var)